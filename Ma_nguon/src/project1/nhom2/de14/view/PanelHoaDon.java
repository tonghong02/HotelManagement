				chk_date.setValue(CalendarAdapter.StringToDateB((String) dtm.getValueAt(selected, 2)));
				tfTienPhong.setText((String) dtm.getValueAt(selected, 3));
				tfTienDichVu.setText((String) dtm.getValueAt(selected, 4));
